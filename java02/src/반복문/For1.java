package 반복문;

public class For1 {
	
	public static void main(String[] args) {
		
		for (int i = 0; i < 3; i++) {
			System.out.println(i);
		}
		
		for (int i = 0; i < 100; i++) {
			System.out.println("내가 반복2 >> " + i);
		}
		
		for (int i = 1; i <= 10; i++) {
			System.out.println("내가 반복3 >> " + i + "번째 실행 중");
		}
	}
}
